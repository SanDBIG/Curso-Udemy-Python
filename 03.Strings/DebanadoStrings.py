cadena = "Este es un ejemplo de Substring (Debanado de Cadenas)"

print(len(cadena)) #da la cantidad de caracteres que tiene el string

print(cadena[2]) #parte desde el 0 y muestra el caracter 2
print(cadena[0 : 10]) #parte desde el 0 y llega a caracter 10
print(cadena[0 : 11]) #parte desde el 0 y llega a caracter 11
print(cadena[20 : ]) #empieza desde el caracter 20 y llega hasta el final
print(cadena[-1]) #empieza desde atras